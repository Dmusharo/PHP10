<?php
//Lesson 5. Task 58
//Формирование строк через циклы
//   .=:   --- сокращенная операация
$str='';
for ($i=0;$i<5;$i++){
    $str .="0";
}
echo $str; // вывод 5-ти нулей

//2
$str='';
for ($i=1;$i<=9;$i++){
    $str=$str.$i;
}
echo $str; //Выведет 123456789

//3
$str='';
for ($i=9;$i>=1;$i--){
    $str=$str.$i;
}
echo $str; //Выведет 987654321
//4
$str='-';
for ($i=1;$i<=9;++$i){
    $str=$str.$i."-";
}
echo $str; //Выведет -1-2-3-4-5-6-7-8-9-
?>



